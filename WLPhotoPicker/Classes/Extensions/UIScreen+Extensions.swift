import UIKit

extension UIScreen {
    
    static var width: CGFloat {
        UIScreen.main.bounds.width
    }
    
    static var height: CGFloat {
        UIScreen.main.bounds.height
    }
    
    static var size: CGSize {
        UIScreen.main.bounds.size
    }
    
}
